namespace ThreeInARow.Core.Abstractions;

/// <summary>
/// АТД: Игровой элемент
/// 
/// НАЗНАЧЕНИЕ:
///   Представляет элемент на игровом поле с определённым типом
/// 
/// ЗАПРОСЫ:
///   GetType() : ElementType
///     Возвращает тип элемента
///     
/// КОМАНДЫ:
///   SetType(type: ElementType)
///     Устанавливает новый тип элемента
///     Предусловие: type является допустимым значением перечисления ElementType
///     Постусловие: после выполнения GetType() возвращает переданный тип
///     
/// ИНВАРИАНТЫ:
///   - Элемент имеет определённый тип в каждый момент времени
///   - Тип элемента является одним из шести допустимых значений ElementType
/// </summary>
public interface IGameElement
{
    // Запросы
    ElementType GetType();

    // Команды
    void SetType(ElementType type);
}